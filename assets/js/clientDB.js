var timelogs = [];

function logtime(eid, timetype){
    var nDate = getFullYear() + '-' + getMonth() + '-' + getDate();
    var nTime = getHours() + ':' + getMinutes();

    switch(timetype){
        case 1: 
            var emptime = [
                {empid: eid, workdate: nDate, timein: nTime, ofl: null, bfl: null, timeout: null}
            ]
            timelogs.push(emptime);
            break;
        case 2:
            for(i = 0; i <= timelogs.length; i++){
                if(timelogs[i][0] == eid && timelogs[i][1] == nDate){
                    timelogs[i][3] = nTime;
                    return;
                }
            }
            break;
        case 3:
            for(i = 0; i <= timelogs.length; i++){
                if(timelogs[i][0] == eid && timelogs[i][1] == nDate){
                    timelogs[i][4] = nTime;
                    return;
                }
            }
            break;
        case 4:
            for(i = 0; i <= timelogs.length; i++){
                if(timelogs[i][0] == eid && timelogs[i][1] == nDate){
                    timelogs[i][5] = nTime;
                    return;
                }
            }   
        default: break;
    }
}


